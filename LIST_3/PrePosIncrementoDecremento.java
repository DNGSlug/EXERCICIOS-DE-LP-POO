public class  PrePosIncrementoDecremento {
	
	public static void main (String[] args) {
				
				
				int a = 1;
		
		
		System.out.println((++a)-1); // 1
		System.out.println((--a)+2 ); // 3
		System.out.println((++a)+1);//3
		System.out.println((++a)-2);//1
		
	}
}

